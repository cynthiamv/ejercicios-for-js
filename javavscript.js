// 1. Mostrar en consola los números del 0 al 10

for (let i = 0; i <= 10; i++) {
    console.log(i);
}


// 2. Mostrar en consola los números del 100 al 0

for (let i = 100; i >= 0; i--) {
    console.log(i);
}


// 3. Mostrar en consola los números pares del 0 al 1000 

for (let i = 0; i <= 1000; i += 2) {
    console.log(i);
}